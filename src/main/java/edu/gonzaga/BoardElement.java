package edu.gonzaga;

public abstract class BoardElement {
    protected int x;
    protected int y;
    protected String texturePath;

    public BoardElement(int x, int y) {
        this.x = x;
        this.y = y;
    }
    
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}
